<template>
  <div id="app">
    <img alt="Vue logo" src="./assets/logo.png">
    <HelloWorld msg="Welcome to Your Vue.js App"/>
    <p>The Server says:</p>
    <p>{{ msg }}</p>
  </div>
</template>

<script>
import HelloWorld from './components/HelloWorld.vue'

export default {
  name: 'App',
  components: {
    HelloWorld
  },
  data: () => ({
    msg: '...',
  }),
  async mounted() {
    const resp = await fetch('http://localhost:9090/api/hello');
    const data = await resp.json();
    this.msg = data.message;
  },
}
</script>

